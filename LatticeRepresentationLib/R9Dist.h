static void imv9(double v1[9], int m[81], double v2[9]) {
   int i, j;
   double sum;
   for (i = 0; i < 9; i++) {
      sum = 0.0;
      for (j = 0; j < 9; j++) {
         sum = sum + ((double)m[9 * i + j]) * v1[j];
      }
      v2[i] = sum;
   }
}

static void r9_diff(double* r1, double* r2, double* diff) {
   for (size_t i = 0; i < 9; ++i) {
      diff[i] = r1[i] - r2[i];
   }
}

static double norm(double* r) {
   double sum;
   sum = 0;
   for (size_t i = 0; i < 9; ++i) {
      sum += r[i]*r[i];
   }
   return sqrt(sum);
}

