S6 CenterOfMass(const std::vector<S6>& v) {
   S6 sum("0 0 0 0 0 0 ");
   for (size_t i = 0; i < 6; ++i)
      sum += v[i];
   return sum / double(v.size( ));
}

std::vector<S6> MoveToCenterOfMass(const std::vector<S6>& v) {
   std::vector<S6> c;
   const S6 cm = CenterOfMass(v);
   for (size_t i = 0; i < v.size( ); ++i)
      c.push_back(v[i] - cm);
   return c;
}

